friends = 10

# friends = friends + 1
friends += 1
print("After += 1  :", friends)

# friends = friends - 2
friends -= 2
print("After -= 2  :", friends)

# friends = friends * 3
friends *= 3
print("After *= 3  :", friends)

# friends = friends / 2
friends /= 2
print("After /= 2  :", friends)

# friends = friends // 2  (floor division)
friends //= 2
print("After //= 2 :", friends)

# friends = friends % 3  (modulus)
friends %= 3
print("After %= 3  :", friends)

# friends = friends ** 2  (exponent)
friends **= 2
print("After **= 2 :", friends)
